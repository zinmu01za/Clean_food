    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       
        try{
            Class.forName("com.mysql.jdbc.Driver");
            String url="jdbc:mysql://localhost/food_database";
            Connection con=DriverManager.getConnection(url,"root","0936408980");
            String sql = "Select *from member where username=? and password = ?";
            PreparedStatement pst = con.prepareStatement(sql);

            pst.setString(1, username.getText());
            pst.setString(2, password.getText());
            ResultSet rs = pst.executeQuery();
            if(rs.next()){
                JOptionPane.showMessageDialog(null, "รหัสถูกต้อง");
                Menu field= new Menu();
                field.setVisible(true);
                setVisible(false);

            }
            else{
                JOptionPane.showMessageDialog(null, "คุณป้อน Username หรือ password ไม่ถูกต้อง");
                username.setText("");
                password.setText("");
            }
            con.close();

        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

